func GetCounts(data [][]uint) ([]uint, uint, uint) {
	totals := data[0]
	bits := uint(len(totals))
	max := uint(len(data))

	for line := uint(1); line < max; line++ {
		for b := uint(0); b < bits; b++ {
			totals[b] += data[line][b]
		}
	}
	return totals, bits, max
}

